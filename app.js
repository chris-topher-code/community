async function loadCommentsForPost(postId) {
    const { data } = await supabaseClient
        .from('comments')
        .select('*, profiles(name, avatar_url, country_code)')
        .eq('post_id', postId)
        .order('created_at', { ascending: true });
    return data || [];
}

